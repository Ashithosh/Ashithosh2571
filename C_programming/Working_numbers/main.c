#include <stdio.h>
#include <stdlib.h>

int main()
{

    printf("%f\n", 5.6 * 7.56);
    printf("%d\n", 5 * 6);
    printf("%f\n", pow(2, 3)); //2^3
    printf("%f\n", sqrt(36));
    printf("%f\n", ceil(36.356)); //round off values next highest number
    printf("%f\n", floor(36.656)); //round off values lowest number
    return 0;
}
